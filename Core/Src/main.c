/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
	// Flag = 0 --> Jump to App
	// Flag = 1 --> Bootloader Mode
	// Check if app mode is enabled. If yes, jump to app
	if(is_app_mode())
		jump_boot_to_app();

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* Configure the system clock */
	SystemClock_Config();

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_DMA_Init();
	MX_ADC1_Init();
	MX_CAN_Init();
	MX_SPI1_Init();
	MX_TIM1_Init();
	MX_TIM2_Init();
	MX_TIM3_Init();
	MX_TIM4_Init();
	MX_USART3_UART_Init();

	boot_init();

	while (1)
	{
		// CAN bus reception and transmission
		can_core_rx();
		can_core_tx();
		// Bootloader core
		boot_core();
	}
}

